                function DFS(root, s) {
                  //用来记录访问过的节点
                  var remark = new Set();
                  //用来存储剩余访问的节点
                  var arr = [];
                  //用来记录深度遍历节点
                  var dfs = [];
                  arr.push(s);
                  //标记访问过的节点
                  remark.add(s);
                  var vet;
                  while (arr.length > 0) {
                    //从栈顶弹出当前需要访问的节点
                    vet = arr.pop();

                    dfs.push(vet);
                    for (var item of root.get(vet)) {
                      //如果当前的邻接表中节点没有访问过 放入栈中，并添加访问记号
                      if (!remark.has(item)) {
                        arr.push(item);
                        remark.add(item);
                      }
                    }
                  }
                  return dfs;
                }

                function arrayHasEl(array, ele) {
                  for (let el of array) {
                    if (el.length == ele.length) {
                      for (let index in el) {
                        if (el[index] !== ele[index]) {
                          break;
                        }
                        if (index == el.length - 1) {
                          return true;
                        }
                      }
                    }
                  }
                  return false;
                }

